function isOpenDelimiter(character: string): boolean {
    switch (character) {
        case "(":
        case "[":
        case "{":
        case "<":
            return true;
        default:
            return false;
    }
}

function isCloseDelimiter(character: string): boolean {
    switch (character) {
        case ")":
        case "]":
        case "}":
        case ">":
            return true;
        default:
            return false;
    }
}

function isValidClosure(lastCharacter: string, currentCharacter: string): boolean {
    return (
        (lastCharacter === "(" && currentCharacter === ")") ||
        (lastCharacter === "[" && currentCharacter === "]") ||
        (lastCharacter === "{" && currentCharacter === "}") ||
        (lastCharacter === "<" && currentCharacter === ">")
    );
}

function syntaxErrorPoints(character: string): number {
    switch (character) {
        case ")":
            return 3;
        case "]":
            return 57;
        case "}":
            return 1197;
        case ">":
            return 25137;
        default:
            return undefined;
    }
}

function dayTenPartOne(): void {
    let score: number = 0;
    for (const line of lines) {
        const stack: string[] = [];
        const chunks: string[] = [];
        const characters: string[] = line.split("").reverse();
        let chunk: string = "";
        while (characters.length > 0) {
            const currentCharacter = characters.pop();
            if (isOpenDelimiter(currentCharacter)) {
                chunk = chunk + currentCharacter;
                stack.push(currentCharacter);
            } else if (isCloseDelimiter(currentCharacter)) {
                const lastCharacter: string = stack.pop();
                if (isValidClosure(lastCharacter, currentCharacter)) {
                    chunk = chunk + currentCharacter;
                } else {
                    score += syntaxErrorPoints(currentCharacter);
                    break;
                }
                if (stack.length === 0) {
                    console.log(chunk);
                    chunks.push(chunk);
                    chunk = "";
                }
            } else {
                // error
            }
        }
    }
    console.log("Score: " + score);
}

